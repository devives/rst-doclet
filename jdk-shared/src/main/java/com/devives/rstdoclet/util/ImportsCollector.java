package com.devives.rstdoclet.util;

import javax.lang.model.element.Element;
import javax.lang.model.element.ExecutableElement;
import javax.lang.model.element.TypeElement;
import javax.lang.model.element.VariableElement;
import javax.lang.model.type.TypeMirror;
import java.util.Map;

public interface ImportsCollector {

    ImportsCollector collect(Element element);

    ImportsCollector collect(ExecutableElement executableElement);

    ImportsCollector collect(VariableElement variableElement);

    ImportsCollector collect(TypeMirror typeMirror);

    ImportsCollector collect(TypeElement typeElement);

    ImportsCollector collect(TypeElement typeElement, boolean withSuperClasses);

    Map<String, TypeElement> getImportsMap();

}
